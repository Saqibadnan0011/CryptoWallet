import SwiftUI

struct HomeView: View {
    
    @EnvironmentObject private var vm: HomeViewModel
    @State private var showProtfolio: Bool = false
    
    var body: some View {
        ZStack {
            Color.theme.background
                .ignoresSafeArea()
            
            VStack {
                homeHeader
                
                columnTitle
                
                if !showProtfolio {
                    allCoinList
                        .transition(.move(edge: .leading))
                }
                if showProtfolio {
                    portfolioCoinList
                        .transition(.move(edge: .trailing))
                }
                Spacer()
            }
        }
    }
}

